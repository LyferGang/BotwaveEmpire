    def _save_report_metadata(self, report_id: str, report_type: ReportType,
                              customer_id: Optional[str], title: str,
                              file_path: Path) -> ReportMetadata:
        """Save report metadata to database."""
        file_size = file_path.stat().st_size
        
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("""
                INSERT INTO reports (id, report_type, customer_id, title, file_path, file_size)
                VALUES (?, ?, ?, ?, ?, ?)
            """, (report_id, report_type.value, customer_id, title, str(file_path), file_size))
            
        return ReportMetadata(
            report_id=report_id,
            report_type=report_type.value,
            customer_id=customer_id,
            title=title,
            generated_at=datetime.now().isoformat(),
            file_path=str(file_path),
            file_size=file_size
        )
        
    def list_reports(self, customer_id: Optional[str] = None,
                    report_type: Optional[str] = None) -> List[Dict[str, Any]]:
        """List all reports, optionally filtered."""
        query = "SELECT * FROM reports WHERE 1=1"
        params = []
        
        if customer_id:
            query += " AND customer_id = ?"
            params.append(customer_id)
        if report_type:
            query += " AND report_type = ?"
            params.append(report_type)
            
        query += " ORDER BY generated_at DESC"
        
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            rows = conn.execute(query, params).fetchall()
            return [dict(row) for row in rows]
            
    def get_report(self, report_id: str) -> Optional[Dict[str, Any]]:
        """Get a specific report by ID."""
        with sqlite3.connect(self.db_path) as conn:
            conn.row_factory = sqlite3.Row
            row = conn.execute(
                "SELECT * FROM reports WHERE id = ?",
                (report_id,)
            ).fetchone()
            return dict(row) if row else None
            
    def delete_report(self, report_id: str) -> bool:
        """Delete a report and its file."""
        report = self.get_report(report_id)
        if not report:
            return False
            
        # Delete file
        try:
            Path(report['file_path']).unlink(missing_ok=True)
        except Exception:
            pass
            
        # Delete from database
        with sqlite3.connect(self.db_path) as conn:
            conn.execute("DELETE FROM reports WHERE id = ?", (report_id,))
            
        return True


